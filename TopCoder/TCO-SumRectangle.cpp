class SumRectangle {
public:
	int getCorner(vector <int> leftColumn, vector <int> topRow) {
		int r = leftColumn.size();
		int c = topRow.size();
		vector<vector<int> > v(r,vector<int>(c));

		for(int i=0; i<c; i++)
			v[0][i] = topRow[i];

		for(int i=0; i<r; i++)
					v[i][0] = leftColumn[i];

		for(int i=1; i<r; i++)
			for(int j=1; j<c; j++)
				v[i][j] = v[i-1][j-1] - v[i][j-1]-v[i-1][j];

		return v[r-1][c-1];

		
	}
};


