bool isprime(int n){
	if(n==2)
		return 1;

	if(n<2 || n%2==0)
		return 0;

	for(int i=2; i*i<=n; i++)
		if(n%i==0)
			return 0;
	return 1;
}

unsigned long long pwr(int i,int q){
	unsigned long long res = 1;
	while(q--)
		res *=i;
	return res;
}
