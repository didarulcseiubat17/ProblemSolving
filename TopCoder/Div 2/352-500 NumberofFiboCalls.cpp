class NumberofFiboCalls {
public:

	int z,  O;
	int memo[50];
	int mem[50][2];

	int fib(int n) {
		if (n == 0) {
			mem[n][0] = ++z;
			return 0;
		}
		if (n == 1) {
			mem[n][1] = ++O;
			return 1;
		}

		if (memo[n] != -1) {
			z += mem[n][0], O += mem[n][1];
			return memo[n];
		}

		memo[n] = fib(n - 1) + fib(n - 2);
		mem[n][0] = z, mem[n][1] = O;
		return memo[n];
	}
	vector<int> fiboCallsMade(int n) {
		z=0,O=0;
		memset(memo, -1, sizeof memo);
		memset(mem,0,sizeof mem);

		fib(n);
		vector<int> x;
		x.push_back(z),x.push_back(O);
		return x;
	}
};

