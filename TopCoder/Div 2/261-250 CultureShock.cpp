class CultureShock {
public:
	string translate(string text) {
		stringstream ss(text);
		string res,s;
		while(ss>>s){
			if(s=="ZEE")
				res+="ZED";
			else
				res+=s;
			if(ss.good())
				res+=" ";
		}

		return res;
	}
};


